package com.alianza.aoc.day02.dataobject;

public class GameSet {
    int red;
    int green;
    int blue;

    public GameSet (int red, int green, int blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }

    public int getRed() {
        return this.red;
    }

    public int getGreen() {
        return this.green;
    }

    public int getBlue() {
        return this.blue;
    }
    
}
